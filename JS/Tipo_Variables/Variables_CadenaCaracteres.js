/* La variable texto almacena un dato tipo carácter */
/* Básicamente letras o texto (permite combinar números y letras) */ 

/* 1 */
var mitexto = "Mi nombre es "; 
mitexto = mitexto + "Juan"; 
alert(mitexto);  //Mi nombre es Juan

/* 2 */
var mitexto = "Hola Mundo!"; 
alert(mitexto);  //Hola Mundo

/* 3 */
var mitexto = "El número es " + 3; 
alert(mitexto);  //El número es 3

/* 4 */
var mitexto = 'El \'libro\' es interesante'; 
alert(mitexto);  // "El 'libro' es interesante" 

/* 5 */
var mitexto = "20" + 3; 
